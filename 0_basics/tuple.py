# Tuple type (Immutable)
monday_temperatures = (1, 4, 5)
print({ 'monday_temperatures': monday_temperatures })

# Nested tuple
color_codes = ((1, 2, 3), (4, 5, 6), (7, 8, 9))

# List vs Tuple
# List is MUTABLE!
num_list = [1, 2, 3, 4, 5]
num_list.append(6)
num_list.remove(2)
print({ 'num_list': num_list })